// Helper function to get churches by state
export const getChurchesByState = (state: string): Church[] => {
  return churchDirectory.filter(church => church.state === state);
};

// Helper function to get churches by city
export const getChurchesByCity = (city: string): Church[] => {
  return churchDirectory.filter(church =>
    church.city.toLowerCase().includes(city.toLowerCase())
  );
};

// Helper function to search churches
export const searchChurches = (query: string): Church[] => {
  const lowerQuery = query.toLowerCase();
  return churchDirectory.filter(church =>
    church.name.toLowerCase().includes(lowerQuery) ||
    church.city.toLowerCase().includes(lowerQuery) ||
    church.state.toLowerCase().includes(lowerQuery) ||
    church.postcode.includes(query) ||
    church.specialties?.some((specialty: string) =>
      specialty.toLowerCase().includes(lowerQuery)
    )
  );
};

export default churchDirectory;