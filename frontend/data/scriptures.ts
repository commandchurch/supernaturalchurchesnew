// Function to get a random scripture
export const getRandomScripture = (): Scripture => {
  const randomIndex = Math.floor(Math.random() * scriptures.length);
  return scriptures[randomIndex];
};

// Function to get scriptures by category
export const getScripturesByCategory = (category: string): Scripture[] => {
  return scriptures.filter(scripture => scripture.category === category);
};

// Function to get scriptures by version
export const getScripturesByVersion = (version: 'NKJV' | 'TPT' | 'AMP'): Scripture[] => {
  return scriptures.filter(scripture => scripture.version === version);
};